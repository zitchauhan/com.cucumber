package ThisJava;
public class IfElseStatements {
    public static void main(String[] args) {
        int x = 10;
        if (x > 0) {
            System.out.println("Positive");
        } else if (x < 0) {
            System.out.println("Negative");
        } else {
            System.out.println("Zero");
        }
    }
}
